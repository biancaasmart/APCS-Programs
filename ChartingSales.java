import java.util.Random;
public class ChartingSales
{
    public static void main(String[] args) //Main method
    {   
        String newS = "$";
        Histogram withSymbol; //Declares new Histogram object that uses symbol
        Histogram noSymbol; //New histogram object that uses default symbol
        System.out.println(" Day     Daily    Sales Graph"); //Prints header
        for(int i = 2; i <= 31; i++) //Loop for every day of the month
        {
            int sales = Random(); //Variable that calls method for new random number
            withSymbol = new Histogram(sales, newS); //Creates new histogram oject with sales as parameter
            noSymbol = new Histogram(sales); //Creates new histogram object with length and no symbol
            if(i == 8 || i == 15 || i == 22 || i == 29) //If day is on sunday
            {
                System.out.println(); //Prints blank line
            }
            else if(i!= 8 && i != 15 && i != 22 && i != 29) //For every other day
            {
                if(i < 10) //Adds extra space for formatting
                {
                    System.out.print(" ");
                }
                System.out.print(" "+i+"      "+sales+"     "+Histogram.print(sales, newS)+"\n"); //Prints output and calls method from Historgram class to print the bar
            }
        }
    }
    public static int Random() //Class for creating new random number
    {
        Random rand = new Random(); //New random object
        int newR = rand.nextInt(40000)+10000; //Integer for random number between 10,000 and 50,000
        return newR; //Returns random number
    }
    
}
